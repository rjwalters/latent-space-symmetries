"""Positive control: verify the pipeline detects group symmetry when it exists.

Embeds exact S_3 representations in synthetic activations at various noise
levels and subspace dimensions, then runs the same operator fitting and
group relation tests used on real model activations.

This establishes:
1. The pipeline correctly identifies exact group structure (relation errors → 0)
2. How noise degrades detection (calibration curve)
3. That real model errors are far above what noisy group structure looks like

Usage:
    python scripts/positive_control.py
    python scripts/positive_control.py --noise-levels 0.0 0.05 0.1 0.2 0.5
"""

